const get = require('lodash/get');

function convert2HierarchyData(data) {
  const map = new Map();
  const pathMap = new Map();

  while (data.length) {
    const v = data.shift();

    if (v == null) break;

    if (v.hierarchyType === 'Category') {
      v.subRows = [];
    }

    // TODO: AL KARAMA is not existed.
    if (v.parentBusinessId === 'AL KARAMA') {
      continue;
    }

    if (v.parentBusinessId === '' || v.parentBusinessId == null) {
      map.set(v.businessId, v);
      pathMap.set(v.businessId, [v.businessId]);
    } else if (pathMap.get(v.parentBusinessId)) {
      const paths = pathMap.get(v.parentBusinessId);
      const [keyInThePaths, ...subPaths] = paths;
      const parent = map.get(keyInThePaths);
      const target = subPaths.length ? get(parent, subPaths.join('.')) : parent;
      target.subRows.push(v);
      pathMap.set(v.businessId, paths.concat('subRows', target.subRows.length - 1));
    } else {
      data.push(v);
    }
  }

  return Array.from(map, ([, value]) => value);
}

